    public static void sendMessages(Player player){
        World world = player.getWorld();
        Location playerLocation = player.getLocation();
        int radius = 19;

        int minX = playerLocation.getBlockX() - radius;
        int minZ = playerLocation.getBlockZ() - radius;
        int maxX = playerLocation.getBlockX() + radius;
        int maxZ = playerLocation.getBlockZ() + radius;

        for (int x = minX; x <= maxX; x++) {
            StringBuilder rowBuilder = new StringBuilder();
            for (int z = maxZ; z >= minZ; z--) {
                Block block = world.getHighestBlockAt(x, z);
                Material material = block.getType();
                Color color = BlockColorMapper.getBlockColor(material);
                rowBuilder.append(net.md_5.bungee.api.ChatColor.of(colorToHexString(color))).append("â¬›");
            }
            Bukkit.broadcastMessage(rowBuilder.toString());
        }
    }

    public static String colorToHexString(Color color) {
        return String.format("#%02x%02x%02x", color.getRed(), color.getGreen(), color.getBlue());
    }

